CREATE OR REPLACE TABLE `CAR` (
    `id` INT(10) NOT NULL AUTO_INCREMENT,
    `name` VARCHAR(255) NOT NULL UNIQUE,
    `engine_id` INT(10) NULL DEFAULT NULL,
    `suspension_id` INT(10) NULL DEFAULT NULL,
    `tires_id` INT(10) NULL DEFAULT NULL,
    `transmission_id` INT(10) NULL DEFAULT NULL,
    `wheels_id` INT(10) NULL DEFAULT NULL,
    `last_updated` TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`),
    FOREIGN KEY (`engine_id`) REFERENCES engine(`id`),
    FOREIGN KEY (`suspension_id`) REFERENCES suspension(`id`),
    FOREIGN KEY (`tires_id`) REFERENCES tires(`id`),
    FOREIGN KEY (`transmission_id`) REFERENCES transmission(`id`),
    FOREIGN KEY (`wheels_id`) REFERENCES wheels(`id`)
);
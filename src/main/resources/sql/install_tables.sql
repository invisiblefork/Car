    `id` INT(10) NOT NULL AUTO_INCREMENT,
    `name` VARCHAR(255) NOT NULL UNIQUE,
    `rpm_range` INT(10) NOT NULL,
    `horsepower` INT(10) NOT NULL,
    `torque` INT(10) NOT NULL,
    `engine_pullys_id` INT(10) NULL,
    `ecu_id` INT(10) NULL,
